#include "libft.h"
#include <stdio.h>

char	*ft_strrchr(const char *string, int symbol)
{
	char *last;

	last = (0);
	while (*string)
	{
		if (*string == symbol)
			last = (char*)string;
		++string;
	}
	if (last)
		return (last);
	if (symbol == '\0')
		return ((char*)string);
	return (0);
}
