#include "libft.h"
#include <stdlib.h>

char	*ft_strtrim(char const *s)
{
	char const *str;

	if (s == NULL)
		return (NULL);
	while (*s == ' ' || *s == '\n' || *s == '\t')
		s++;
	if (*s == '\0')
		return (ft_strnew(0));
	str = s + ft_strlen(s) - 1;
	while (ft_iswhitespace(*str))
		str--;
	return (ft_strsub(s, 0, str - s + 1));
}
