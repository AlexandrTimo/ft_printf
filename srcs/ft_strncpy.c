#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t len)
{
	char	*cur;

	cur = dst;
	while (len && *src)
	{
		*cur++ = *src++;
		--len;
	}
	if (len)
		ft_nmemset(cur, '\0', len);
	return (dst);
}
